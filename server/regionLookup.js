// 시·도명·시명은 앞에 한글이 붙으면 다른 단어의 일부다(예: "해운대구"의 "대구", "천안산"의 "안산").
// 그래서 단어 경계(문자열 시작·비한글 앞글자, 또는 국립/시립 같은 기관 접두어)에서만 매칭한다.
function hasBoundary(text, name) {
  return new RegExp(`(^|[^가-힣]|국립|시립|도립|사립|공립)${name}`).test(text);
}

// 흔한 단어와 겹치는 지명용: 이름 뒤에 '시'나 '군'이 붙은 경우만 지역으로 인정한다(앞도 단어 경계).
function hasCitySuffix(text, name) {
  return new RegExp(`(^|[^가-힣])${name}(시|군)`).test(text);
}

// 원문에서 지역명(구·군, 시·도, 시)을 찾아 매핑되는 시·도 배열을 반환한다(중복 제거).
// 못 찾거나 겹치는 이름뿐이면 빈 배열 = "모른다=무관"이라 모두에게 보인다.
// (지역을 잘못 붙여 전국 공고를 특정 지역으로 제외하는 것보다, 안 붙여 무관으로 두는 쪽이 안전하다)
export function deriveRegionFromDistrict(text) {
  const t = text || "";
  // '전국' 대상임을 명시하면 개최지가 어디든 오픈이라 지역 제한이 아니다. 무관으로 둬 모두에게 보이게 한다
  // (전국 공고를 개최지로 잘못 묶어 제외하는 것 방지 - "제외될 바엔 뜨는 게 낫다").
  if (/전국/.test(t)) return [];
  const found = new Set();
  for (const [district, region] of Object.entries(DISTRICT_TO_REGION)) {
    if (t.includes(district)) found.add(region);
  }
  for (const [city, region] of Object.entries(CITY_TO_REGION)) {
    if (hasBoundary(t, city)) found.add(region);
  }
  for (const [city, region] of Object.entries(CITY_SUFFIX_TO_REGION)) {
    if (hasCitySuffix(t, city)) found.add(region);
  }
  for (const [prov, region] of Object.entries(PROVINCE_TO_REGION)) {
    if (hasBoundary(t, prov)) found.add(region);
  }
  // 세종: '세종'만 있으면 세종대왕·세종사이버대(전국)일 수 있어 '세종시/세종특별자치시'일 때만 충청으로 본다.
  if (/(^|[^가-힣])세종(특별자치)?시/.test(t)) found.add("충청");
  // 고성: 강원 고성군 + 경남 고성군 둘 다라 '고성군'이면 둘 다 태그(양쪽 유저 다 보이게, 무관 지역만 제외).
  if (hasCitySuffix(t, "고성")) { found.add("강원"); found.add("경상"); }
  // '광주'(광주광역시): 축약형 '광주'는 경기 광주시와 겹치니 '경기'가 같이 없고 단어 경계일 때만 광주광역시로.
  if (hasBoundary(t, "광주") && !/경기/.test(t)) found.add("광주");
  return [...found];
}
