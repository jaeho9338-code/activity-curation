// 시·도명, 시명은 앞에 한글이 붙으면 다른 단어의 일부다(예: "해운대구"의 "대구", "천안산"의 "안산").
// 그래서 단어 경계(문자열 시작 또는 앞 글자가 한글이 아님)에서만 매칭한다. 구·군명은 3자 이상 특정 이름이라
// 부분문자열 충돌이 거의 없어 단순 포함으로 둔다.
// 앞 글자가 한글이 아니면(문자열 시작·공백·괄호 등) 단어 경계로 본다. 추가로 국립/시립 같은 기관 접두어
// 뒤도 경계로 인정한다("국립순천대학교"의 순천). "해운대구"의 대구처럼 접두어가 아닌 한글 뒤는 안 잡는다.
function hasBoundary(text, name) {
  return new RegExp(`(^|[^가-힣]|국립|시립|도립|사립|공립)${name}`).test(text);
}

// 원문에서 지역명(구·군, 시·도, 시)을 찾아 매핑되는 시·도 배열을 반환한다(중복 제거).
// 못 찾거나 겹치는 이름뿐이면 빈 배열 - "모른다"는 뜻이라 확인 필요로 남는다.
export function deriveRegionFromDistrict(text) {
  const t = text || "";
  const found = new Set();
  for (const [district, region] of Object.entries(DISTRICT_TO_REGION)) {
    if (t.includes(district)) found.add(region);
  }
  for (const [city, region] of Object.entries(CITY_TO_REGION)) {
    if (hasBoundary(t, city)) found.add(region);
  }
  for (const [prov, region] of Object.entries(PROVINCE_TO_REGION)) {
    if (hasBoundary(t, prov)) found.add(region);
  }
  // '광주'(광주광역시): 명시적 '광주광역시'는 위에서 이미 잡힘. 축약형 '광주'는 경기 광주시와 겹치니,
  // '경기'가 같이 없고 단어 경계일 때만 광주광역시로 본다(경기 광주면 판별 보류).
  if (hasBoundary(t, "광주") && !/경기/.test(t)) found.add("광주");
  return [...found];
}
